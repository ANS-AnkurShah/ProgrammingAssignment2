##Generate function makeCacheMatrix that captures the matrix's inverse value

makeCacheMatrix<- function(x = matrix()) {
invrs<- NULL  ##set initial value of invrs as NULL, i.e. invrs will hold the value of the matrix's inverse
set<- function(y) { ##define 'set' function' to assign new matrix value in the makeCacheMatrix parent function
x<<- y
invrs<<- NULL ##if there's a new matrix, then invrs is reset to NULL
}

get<- function()x ##defines 'get' function to return the value generated by the matrix argument

setinverse<- function(inverse)invrs<<- inverse ##assign the value of invrs in the makeCacheMatrix parent function
getinverse<- function()invrs      ##will get the value of invrs when the function is called
list(set = set, get = get, setinverse = setinverse, getinverse = getinverse) ##use list function in order to refer to the functions when called using $ operator
}
