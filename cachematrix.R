
## function"makeCacheMatrix"creats a list which contains 4 functions

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinverse <- function(inverse) m <<- inverse
    getinverse <- function() m
    list(set = set, get = get,
         setinverse = setinverse,
         getinverse = getinverse)
}


## this function receives the list from the previous function"makeCacheMatrix"
## then judge whether the invers matrix has been calculated
## if not, the inverse matrix will be calculated and printed
## if the inverse matrix has been computed, it will be retrived and printed

cacheSolve <- function(x, ...) {
    m <- x$getinverse()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data)
    x$setinverse(m)
    m
}


