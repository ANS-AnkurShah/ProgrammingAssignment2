## The set of makeCacheMatrix and cacheSolve enable
## computation of matrix inverse directly from 
## pre-stored information


## Create Cache Matrix and enable extraction 
## from future computation
makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinv <- function(inv) m <<- inv
  getinv <- function() m
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}
## Check the potential of using pre-stored 
## computation. If so, pre-stored information
## will be used
cacheSolve <- function(x, ...) {
  m <- x$getinv()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinv(m)
  m
}
