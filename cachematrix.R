makeCacheMatrix <- function(x = matrix()) {
		#init matrix inversion output       
		i <- NULL
		
		#def the setter of the input of the object
        set <- function(y) {
                x <<- y
				# if the a new matrix is set then init the inversion to avoid incoherence
                i <<- NULL
        }
		
		#def the getter of the input of the object
        get <- function() x
		
		#def the setter the attribut of the object
        setinverse <- function(inv) i <<- inv
		
		#def the getter the attribut of the object
        getinverse <- function() i
		
		#return of the function (output as a list with all functions)
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)
        
}


