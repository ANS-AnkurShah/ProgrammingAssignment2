## Creates a “matrix” object and then calculates inverse of matrix

## sets value of matrix, gets value of matrix
## sets value of inverse matrix, gets value of inverse matrix

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set,
       get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## computes the inverse of the special “matrix” returned by makeCacheMatrix 
## If the inverse has already been calculated, cacheSolve retrieves the 
## inverse from the cache

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if (!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}
