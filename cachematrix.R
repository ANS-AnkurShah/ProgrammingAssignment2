## Compute the inverse of a matrix, cache the inverse 
## Use / return cached data if matrix has not changed


## Creates matrix x to cache the inverse of x
## Stores and returns matrix x, and its inverse

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinverse <- function(solve) m <<- solve
        getinverse <- function() m
        list(set = set, get = get, setinverse = setinverse, getinverse = getinverse)
}


## Computes and returns inverse of a new matrix;
## Returns previously stored inverse of an existing matrix, and message "getting cached data"

cacheSolve <- function(x, ...) {
        m <- x$getinverse()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinverse(m)
        m
}
