cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
		
		# get the attribut i of the object generated by makeCacheMatrix
		     i <- x$getinverse()
        if(!is.null(i)) {
		
		# if the inversion is already calculated get the value form the cache
                message("getting cached data")
                return(i)
        }
		
		# else calculate the inversion with the function solve
        data <- x$get()
        i <- solve(data, ...)
		
		# set the value calculated as the attribut i to the the object generated by makeCacheMatrix using the setter function
        x$setinverse(i)
		
		# return the matrix inversion
        i
}
